def multiply_two_numbers(a, b):
    """Return the product of a and b.
    
    >>> multiply_two_numbers(2, 3)
    6
    >>> multiply_two_numbers(-1, 5)
    -5
    >>> multiply_two_numbers(0, 10)
    0
    >>> multiply_two_numbers(7, 0)
    0
    >>> multiply_two_numbers(0, 0)
    0
    """
    return a * b
